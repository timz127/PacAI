    def __init__(self, startingGameState):
        super().__init__()

        self.walls = startingGameState.getWalls()
        self.startingPosition = startingGameState.getPacmanPosition()
        top = self.walls.getHeight() - 2
        right = self.walls.getWidth() - 2

        self.corners = ((1, 1), (1, top), (right, 1), (right, top))
        for corner in self.corners:
            if not startingGameState.hasFood(*corner):
                logging.warning('Warning: no food in corner ' + str(corner))

        # *** Your Code Here ***
        self._visitedCorners = []
        # raise NotImplementedError()

    def startingState(self):
        # returns the starting state of the graph
        # states consist of location and list of visited corners which is empty at the start

        return (self.startingPosition, [])

    def isGoal(self, state):
        # returns true if at the current position
        # if is a corner and it has traversed through every corner

        curNode = state[0]
        self._visitedCorners = state[1]

        if curNode in self.corners:
            if curNode not in self._visitedCorners:
                self._visitedCorners.append(curNode)
            return len(self._visitedCorners) == len(self.corners)
        return False

    def successorStates(self, state):
        # returns a list of successors to the current state
        # each successor include state, action, and cost

        self._visitedCorners = state[1]
        successors = []
        self._numExpanded += 1

        for action in Directions.CARDINAL:
            x, y = state[0]
            dx, dy = Actions.directionToVector(action)
            nextx, nexty = int(x + dx), int(y + dy)
            hitsWall = self.walls[nextx][nexty]

            if (not hitsWall):
                # Construct the successor.
                sVistedCorners = self._visitedCorners.copy()
                nextNode = (nextx, nexty)
                if nextNode in self.corners:
                    if nextNode not in sVistedCorners:
                        sVistedCorners.append(nextNode)
                successors.append(((nextNode, sVistedCorners), action, 1))

        return successors

    def actionsCost(self, actions):
        """
        Returns the cost of a particular sequence of actions.
        If those actions include an illegal move, return 999999.
        This is implemented for you.
        """

        if (actions is None):
            return 999999

        x, y = self.startingPosition
        for action in actions:
            dx, dy = Actions.directionToVector(action)
            x, y = int(x + dx), int(y + dy)
            if self.walls[x][y]:
                return 999999

        return len(actions)

